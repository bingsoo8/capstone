class NSFNetEnv(gym.Env):
    """
    NSFNet 기반 강화학습 환경 (Gymnasium 표준)

    [Action]  : 각 링크(엣지)의 Dijkstra 비용 가중치 벡터 (shape: 21,)
                값이 클수록 해당 링크를 회피하는 경로가 선택됨
                범위: 1.0 ~ 100.0

    [Obs]     : 각 링크의 현재 사용률 벡터 (shape: 21,)
                0.0 = 여유, 1.0 = 포화

    [Reward]  : delivered - dropped * 10 - avg_delay * 0.1
                (팀원이 가중치 튜닝 필요)

    [Episode] : 200틱 = 1에피소드
    """
    metadata = {"render_modes": ["human"]}

    def __init__(self, graph_seed=42):
        super().__init__()

        # FIX: 그래프를 고정 seed로 한 번만 생성
        # → 에피소드마다 같은 네트워크 토폴로지 사용 (공정한 비교)
        random.seed(graph_seed)
        self.graph = build_nsfnet()

        self.edges    = list(self.graph.edges())
        self.num_edges = len(self.edges)
        self.max_ticks = 200

        self.action_space = spaces.Box(
            low=1.0, high=100.0,
            shape=(self.num_edges,), dtype=np.float32
        )
        self.observation_space = spaces.Box(
            low=0.0, high=1.0,
            shape=(self.num_edges,), dtype=np.float32
        )

        # 라우팅 테이블 갱신 주기 (틱)
        # FIX: 매 틱마다 Dijkstra 재계산은 과부하
        # → N틱마다 한 번만 갱신 (협의해서 조정)
        self.routing_update_interval = 5

    def reset(self, seed=None, options=None):
        super().reset(seed=seed)

        # 트래픽·라우터만 초기화 (그래프는 고정)
        if seed is not None:
            random.seed(seed)

        self.current_tick = 0
        self.traffic      = TrafficGenerator(self.graph)
        self.router       = DynamicRouter(self.graph)

        # 초기 라우팅 테이블: 균등 가중치
        initial_weights = {
            (min(u,v), max(u,v)): 1.0
            for u, v in self.edges
        }
        self.router.update_routing_table(initial_weights)

        obs  = np.zeros(self.num_edges, dtype=np.float32)
        info = {}
        return obs, info

    def step(self, action):
        # FIX: N틱마다만 라우팅 테이블 갱신 (Dijkstra 과호출 방지)
        if self.current_tick % self.routing_update_interval == 0:
            edge_weights = {
                (min(self.edges[i][0], self.edges[i][1]),
                 max(self.edges[i][0], self.edges[i][1])): float(action[i])
                for i in range(self.num_edges)
            }
            self.router.update_routing_table(edge_weights)

        # 트래픽 발생 및 인큐
        # FIX: 초기 enqueue 드랍도 카운트
        init_dropped = 0
        for pkt in self.traffic.generate(self.current_tick):
            success, _ = self.router.enqueue(pkt.src, pkt)
            if not success:
                init_dropped += 1

        # 1틱 시뮬레이션
        delivered, dropped, avg_delay, util_map = self.router.process_tick()
        total_dropped = dropped + init_dropped

        # Observation
        obs = np.array([
            util_map.get(
                (min(e[0],e[1]), max(e[0],e[1])), 0.0
            )
            for e in self.edges
        ], dtype=np.float32)

        # Reward
        # 설계한 가중치로 교체 필요
        reward = (
            delivered   * 1.0
            - total_dropped * 10.0
            - avg_delay     * 0.1
        )

        self.current_tick += 1
        terminated = self.current_tick >= self.max_ticks
        truncated  = False

        info = {
            "tick":          self.current_tick,
            "delivered":     delivered,
            "dropped":       total_dropped,
            "avg_delay":     avg_delay,
        }

        return obs, reward, terminated, truncated, info


