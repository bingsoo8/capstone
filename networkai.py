# 2. Dynamic Router
# ─────────────────────────
class DynamicRouter:
    def __init__(self, graph, queue_capacity=10):
        self.graph = graph
        self.queue_capacity = queue_capacity
        self.queues = defaultdict(list)
        self.routing_table = {}
        self.link_load = defaultdict(float)

    def update_routing_table(self, edge_weights):
        """
        AI가 결정한 링크별 가중치로 Dijkstra를 실행해 라우팅 테이블을 갱신.
        edge_weights: { (min_u, max_v): cost, ... }  ← 정규화된 키
        """
        table = defaultdict(dict)
        for s in self.graph.nodes:
            dist = {n: float('inf') for n in self.graph.nodes}
            prev = {n: None for n in self.graph.nodes}
            dist[s] = 0
            heap = [(0, s)]

            while heap:
                d, u = heapq.heappop(heap)
                if d > dist[u]:
                    continue
                for v in self.graph.neighbors(u):
                    norm_edge = (min(u, v), max(u, v))
                    cost = edge_weights.get(norm_edge, 1.0)
                    nd = d + cost
                    if nd < dist[v]:
                        dist[v] = nd
                        prev[v] = u
                        heapq.heappush(heap, (nd, v))

            for d in self.graph.nodes:
                if s == d:
                    continue
                path, cur = [], d
                while cur is not None:
                    path.append(cur)
                    cur = prev[cur]
                path.reverse()
                if path and path[0] == s:
                    table[s][d] = path[1]

        self.routing_table = table

    def enqueue(self, node, pkt):
        """
        패킷을 링크별 큐에 삽입.
        반환값: (성공 여부, 드랍 사유)
          - (True,  None)         : 정상 삽입
          - (False, 'no_route')   : 경로 없음
          - (False, 'queue_full') : 큐 포화
        """
        nxt = self.routing_table.get(node, {}).get(pkt.dst)
        if nxt is None:
            pkt.is_dropped = True
            return False, 'no_route'

        q_key = (node, nxt)
        if len(self.queues[q_key]) >= self.queue_capacity:
            pkt.is_dropped = True
            return False, 'queue_full'

        self.queues[q_key].append(pkt)
        return True, None

    def process_tick(self):
        """
        1틱 진행: 모든 링크 큐에서 패킷을 처리하고 결과를 반환.
        반환값: (delivered, dropped, avg_delay, util_map)
        """
        link_bw = {}
        for u, v in self.graph.edges():
            bw = self.graph[u][v]['bandwidth']
            link_bw[(u, v)] = bw
            link_bw[(v, u)] = bw

        self.link_load = defaultdict(float)
        delivered, dropped = 0, 0
        delay_sum = 0.0

        nodes = list(self.graph.nodes)
        random.shuffle(nodes)

        for node in nodes:
            out_hops = [k[1] for k in list(self.queues.keys()) if k[0] == node]
            random.shuffle(out_hops)

            for nxt in out_hops:
                q_key = (node, nxt)
                pending = []

                while self.queues[q_key]:
                    pkt = self.queues[q_key].pop(0)

                    # 목적지 도달
                    if pkt.current_node == pkt.dst:
                        delivered += 1
                        delay_sum += pkt.total_delay
                        continue

                    edge_key = (pkt.current_node, nxt)

                    if link_bw[edge_key] >= pkt.size_mb:
                        prop_d = self.graph[pkt.current_node][nxt]['delay']
                        bw     = self.graph[pkt.current_node][nxt]['bandwidth']
                        c_load = bw - link_bw[edge_key] + pkt.size_mb
                        hop_d  = compute_queuing_delay(prop_d, bw, c_load)

                        link_bw[edge_key]                        -= pkt.size_mb
                        link_bw[(nxt, pkt.current_node)]         -= pkt.size_mb
                        norm_key = (min(pkt.current_node, nxt),
                                    max(pkt.current_node, nxt))
                        self.link_load[norm_key]                 += pkt.size_mb

                        pkt.current_node = nxt
                        pkt.path_taken.append(nxt)
                        pkt.total_delay  += hop_d

                        if pkt.current_node != pkt.dst:
                            # FIX: enqueue 반환값으로 드랍 카운트
                            success, _ = self.enqueue(nxt, pkt)
                            if not success:
                                dropped += 1
                        else:
                            delivered += 1
                            delay_sum += pkt.total_delay
                    else:
                        pending.append(pkt)

                self.queues[q_key] = pending

        avg_delay = delay_sum / delivered if delivered else 0.0

        util_map = {}
        for u, v in self.graph.edges():
            bw = self.graph[u][v]['bandwidth']
            norm_key = (min(u, v), max(u, v))
            load = self.link_load.get(norm_key, 0.0)
            util_map[norm_key] = min(load / bw, 1.0) if bw > 0 else 0.0

        return delivered, dropped, avg_delay, util_map


