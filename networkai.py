# 1. Packet & Graph Builder
# ─────────────────────────
@dataclass
class Packet:
    packet_id: int
    src: int
    dst: int
    size_mb: float
    create_time: float
    current_node: int = field(init=False)
    path_taken: list = field(default_factory=list)
    total_delay: float = 0.0
    is_dropped: bool = False

    def __post_init__(self):
        self.current_node = self.src
        self.path_taken.append(self.src)


def build_nsfnet():
    G = nx.Graph()
    G.add_nodes_from(range(14))
    edges = [
        (0,1),(0,2),(0,7),(1,2),(1,3),(2,5),(3,4),(3,10),
        (4,5),(4,6),(5,9),(5,13),(6,7),(7,8),(8,9),(8,11),
        (9,10),(9,12),(10,11),(11,12),(12,13),
    ]
    for u, v in edges:
        G.add_edge(u, v,
                   bandwidth=random.uniform(100, 500),
                   delay=random.uniform(1, 10))
    return G


class TrafficGenerator:
    def __init__(self, graph, burst_ticks=range(100, 150)):
        self.graph = graph
        self.burst_ticks = burst_ticks
        self._pid = 0

    def generate(self, tick):
        packets = []
        nodes = list(self.graph.nodes)
        for _ in range(5):
            s, d = random.choice(nodes), random.choice(nodes)
            if s != d:
                packets.append(Packet(self._pid, s, d, 1, tick))
                self._pid += 1
        if tick in self.burst_ticks:
            for _ in range(40):
                s = random.choice([3, 9])
                d = random.choice(nodes)
                if s != d:
                    packets.append(Packet(self._pid, s, d, 2, tick))
                    self._pid += 1
        return packets


def compute_queuing_delay(prop_delay, bandwidth, current_load):
    if bandwidth <= 0:
        return float('inf')
    rho = min(current_load / bandwidth, 0.9999)
    return prop_delay + prop_delay * (rho / (1.0 - rho))


