# 4. 비선형 큐잉 지연 계산 함수
# ──────────────────────────────
def compute_queuing_delay(prop_delay, bandwidth, current_load):
    if bandwidth <= 0:
        return float('inf')
    rho = min(current_load / bandwidth, 0.9999)
    queuing_delay = prop_delay * (rho / (1.0 - rho))
    return prop_delay + queuing_delay


