def plot_tick_log(log):
    ticks = [x["tick"]      for x in log]
    drop  = [x["dropped"]   for x in log]
    delay = [x["avg_delay"] for x in log]

    fig, (ax1, ax2) = plt.subplots(2, 1, figsize=(12, 7), sharex=True)
    fig.suptitle("OSPF Static Routing – Per-Tick Performance", fontsize=14, fontweight='bold')

    ax1.fill_between(ticks, drop, alpha=0.3, color='tomato')
    ax1.plot(ticks, drop, color='tomato', linewidth=1.5, label='Packet Drop / tick')
    ax1.axvspan(100, 149, alpha=0.12, color='red', label='Burst Traffic (tick 100~149)')
    ax1.set_ylabel("Dropped Packets")
    ax1.legend(fontsize=9)
    ax1.grid(axis='y', linestyle='--', alpha=0.5)

    ax2.fill_between(ticks, delay, alpha=0.3, color='steelblue')
    ax2.plot(ticks, delay, color='steelblue', linewidth=1.5, label='Avg Delay (ms)')
    ax2.axvspan(100, 149, alpha=0.12, color='red', label='Burst Traffic (tick 100~149)')
    ax2.set_xlabel("Tick")
    ax2.set_ylabel("Avg E2E Delay (ms)")
    ax2.legend(fontsize=9)
    ax2.grid(axis='y', linestyle='--', alpha=0.5)

    plt.tight_layout()
    plt.show()


def plot_network_congestion(graph, tick_log, tick=None):
    if tick is None:
        entry = tick_log[-1]
    else:
        entry = next((x for x in tick_log if x["tick"] == tick), tick_log[-1])

    util_map = entry.get("utilization", {})
    pos = nx.spring_layout(graph, seed=7)

    edge_colors, edge_widths, edge_labels = [], [], {}

    for u, v in graph.edges():
        util = util_map.get((u, v), util_map.get((v, u), 0.0))

        if util <= 0.50:
            color, width = 'limegreen', 2.0
        elif util <= 0.80:
            color, width = 'orange', 3.5
        else:
            color, width = 'crimson', 5.5

        edge_colors.append(color)
        edge_widths.append(width)

        prop_d  = graph[u][v]['delay']
        bw      = graph[u][v]['bandwidth']
        load    = util * bw
        q_delay = compute_queuing_delay(prop_d, bw, load)
        edge_labels[(u, v)] = f"{util*100:.0f}%\n{q_delay:.1f}ms"

    fig, ax = plt.subplots(figsize=(13, 9))
    ax.set_title(
        f"NSFNET Congestion Map  |  Tick {entry['tick']}\n"
        f"Drop: {entry['dropped']}  |  Avg Delay: {entry['avg_delay']:.2f} ms",
        fontsize=13, fontweight='bold', pad=14
    )
    nx.draw_networkx_nodes(graph, pos, ax=ax,
                           node_color='#4A90D9', node_size=700, alpha=0.95)
    nx.draw_networkx_labels(graph, pos, ax=ax,
                            font_color='white', font_size=10, font_weight='bold')
    nx.draw_networkx_edges(graph, pos, ax=ax,
                           edge_color=edge_colors, width=edge_widths, alpha=0.85)
    nx.draw_networkx_edge_labels(graph, pos, edge_labels=edge_labels, ax=ax,
                                 font_size=7.5,
                                 bbox=dict(boxstyle='round,pad=0.2', fc='white', alpha=0.75))
    patches = [
        mpatches.Patch(color='limegreen', label='여유 (≤ 50%)'),
        mpatches.Patch(color='orange',    label='주의 (50~80%)'),
        mpatches.Patch(color='crimson',   label='혼잡 (> 80%)'),
    ]
    ax.legend(handles=patches, loc='lower left', fontsize=10, framealpha=0.9)
    ax.axis('off')
    plt.tight_layout()
    plt.show()


def plot_utilization_heatmap(graph, tick_log):
    edges   = list(graph.edges())
    n_ticks = len(tick_log)

    matrix = np.zeros((len(edges), n_ticks))
    for t_idx, entry in enumerate(tick_log):
        util_map = entry.get("utilization", {})
        for e_idx, (u, v) in enumerate(edges):
            matrix[e_idx, t_idx] = util_map.get((u, v), util_map.get((v, u), 0.0))

    fig, ax = plt.subplots(figsize=(14, 7))
    im = ax.imshow(matrix, aspect='auto', cmap='RdYlGn_r',
                   vmin=0, vmax=1, interpolation='nearest')
    ax.set_yticks(range(len(edges)))
    ax.set_yticklabels([f"{u}↔{v}" for u, v in edges], fontsize=8)
    ax.set_xlabel("Tick")
    ax.set_title("Link Utilization Heatmap  (Red = Congested, Green = Free)",
                 fontweight='bold')
    cbar = fig.colorbar(im, ax=ax, fraction=0.03, pad=0.02)
    cbar.set_label("Utilization", fontsize=10)
    ax.axvline(x=100, color='white', linewidth=1.5, linestyle='--', alpha=0.8)
    ax.axvline(x=149, color='white', linewidth=1.5, linestyle='--', alpha=0.8)
    # FIX: transAxes 좌표계 사용 (transData + y=-1.2 는 그래프 밖으로 나감)
    ax.text(0.54, 1.02, "← Burst →", ha='center', color='gray', fontsize=9,
            transform=ax.transAxes)
    plt.tight_layout()
    plt.show()


