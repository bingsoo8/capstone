# 5. OSPF
# ─────────────────────────
class OSPFStatic:
    def __init__(self, graph, queue_capacity=10):
        self.graph = graph
        self.queue_capacity = queue_capacity
        self.queues = defaultdict(list)   # key: (현재노드, 다음노드) 링크별 독립 큐
        self._tick_drop_count = 0

        self.routing_table = self._build_routing_table()
        self._init_stats()
        self.link_load = defaultdict(float)

    # 대역폭 기반 비용으로 Dijkstra 실행
    def _dijkstra(self, source):
        dist = {n: float('inf') for n in self.graph.nodes}
        prev = {n: None for n in self.graph.nodes}
        dist[source] = 0
        heap = [(0, source)]

        while heap:
            d, u = heapq.heappop(heap)
            if d > dist[u]:
                continue
            for v in self.graph.neighbors(u):
                bw = self.graph[u][v]['bandwidth']
                cost = 10000.0 / bw      # 대역폭이 클수록 비용이 낮음
                nd = d + cost
                if nd < dist[v]:
                    dist[v] = nd
                    prev[v] = u
                    heapq.heappush(heap, (nd, v))
        return prev

    def _build_routing_table(self):
        table = defaultdict(dict)
        for s in self.graph.nodes:
            prev = self._dijkstra(s)
            for d in self.graph.nodes:
                if s == d:
                    continue
                path, cur = [], d
                while cur is not None:
                    path.append(cur)
                    cur = prev[cur]
                path.reverse()
                if path and path[0] == s:
                    table[s][d] = path[1]
        return table

    def get_next_hop(self, cur, dst):
        return self.routing_table.get(cur, {}).get(dst)

    def enqueue(self, node, pkt):
        # FIX: 목적지 도달 패킷은 enqueue 전에 처리해야 하므로
        # 여기서는 목적지가 아닌 패킷만 들어온다고 가정
        nxt = self.get_next_hop(node, pkt.dst)
        if nxt is None:
            pkt.is_dropped = True
            self.stats["dropped"] += 1
            self._tick_drop_count += 1
            return False

        q_key = (node, nxt)
        if len(self.queues[q_key]) >= self.queue_capacity:
            pkt.is_dropped = True
            self.stats["dropped"] += 1
            self._tick_drop_count += 1
            return False

        self.queues[q_key].append(pkt)
        return True

    def step(self, tick):
        self._tick_drop_count = 0

        link_bw = {}
        for u, v in self.graph.edges():
            bw = self.graph[u][v]['bandwidth']
            link_bw[(u, v)] = bw
            link_bw[(v, u)] = bw

        # FIX: 링크 사용량은 정규화된 단방향 키로만 기록
        self.link_load = defaultdict(float)

        delivered = 0
        delay_sum = 0.0

        # 처리 순서 랜덤화로 공평성 확보
        nodes = list(self.graph.nodes)
        random.shuffle(nodes)

        for node in nodes:
            out_hops = [k[1] for k in list(self.queues.keys()) if k[0] == node]
            random.shuffle(out_hops)

            for nxt in out_hops:
                q_key = (node, nxt)
                pending = []

                while self.queues[q_key]:
                    pkt = self.queues[q_key].pop(0)

                    # 목적지 도달 처리
                    if pkt.current_node == pkt.dst:
                        self.stats["delivered"] += 1
                        delay_sum += pkt.total_delay
                        delivered += 1
                        continue

                    edge_key = (pkt.current_node, nxt)

                    if link_bw[edge_key] >= pkt.size_mb:
                        prop_delay = self.graph[pkt.current_node][nxt]['delay']
                        bandwidth  = self.graph[pkt.current_node][nxt]['bandwidth']
                        current_load = bandwidth - link_bw[edge_key] + pkt.size_mb
                        hop_delay    = compute_queuing_delay(prop_delay, bandwidth, current_load)

                        link_bw[edge_key]                                    -= pkt.size_mb
                        link_bw[(nxt, pkt.current_node)]                     -= pkt.size_mb

                        # FIX: 단방향 정규화 키로만 기록 (이중 계산 방지)
                        norm_key = (min(pkt.current_node, nxt),
                                    max(pkt.current_node, nxt))
                        self.link_load[norm_key] += pkt.size_mb

                        pkt.current_node = nxt
                        pkt.path_taken.append(nxt)
                        pkt.total_delay += hop_delay

                        # 목적지가 아니면 다음 링크 큐에 삽입
                        if pkt.current_node != pkt.dst:
                            self.enqueue(nxt, pkt)
                        else:
                            # 목적지 도달: 다음 틱 처리를 위해 도착 큐에 넣음
                            # (delivered는 다음 step()에서 pop 시 처리됨)
                            # → 즉시 처리 방식으로 변경: 바로 카운트
                            self.stats["delivered"] += 1
                            delay_sum += pkt.total_delay
                            delivered += 1
                    else:
                        pending.append(pkt)

                # FIX: pop(0)으로 이미 큐가 비워졌으므로 pending만 남기면 됨
                self.queues[q_key] = pending

        avg_delay = delay_sum / delivered if delivered else 0.0

        # FIX: 정규화된 단방향 키로 사용률 계산 (이중 계산 제거)
        utilization_snapshot = {}
        for u, v in self.graph.edges():
            bw = self.graph[u][v]['bandwidth']
            norm_key = (min(u, v), max(u, v))
            load = self.link_load.get(norm_key, 0.0)
            utilization_snapshot[(u, v)] = min(load / bw, 1.0) if bw > 0 else 0.0

        self.tick_log.append({
            "tick":        tick,
            "dropped":     self._tick_drop_count,
            "delivered":   delivered,
            "avg_delay":   avg_delay,
            "utilization": utilization_snapshot,
        })

    def _init_stats(self):
        self.stats    = {"delivered": 0, "dropped": 0}
        self.tick_log = []


