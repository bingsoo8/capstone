# 3. Traffic Generator
# ─────────────────────────
class TrafficGenerator:
    def __init__(self, graph, burst_ticks=range(100, 150)):
        self.graph = graph
        self.burst_ticks = burst_ticks
        self._pid = 0

    def generate(self, tick):
        packets = []
        nodes = list(self.graph.nodes)

        for _ in range(5):
            s, d = random.choice(nodes), random.choice(nodes)
            if s != d:
                packets.append(Packet(self._pid, s, d, 1, tick))
                self._pid += 1

        if tick in self.burst_ticks:
            for _ in range(40):
                s = random.choice([3, 9])
                d = random.choice(nodes)
                if s != d:
                    packets.append(Packet(self._pid, s, d, 2, tick))
                    self._pid += 1

        return packets


