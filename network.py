# ─────────────────────────
# 1. Packet
# ─────────────────────────
@dataclass
class Packet:
    packet_id: int
    src: int
    dst: int
    size_mb: float
    create_time: float

    current_node: int = field(init=False)
    path_taken: list = field(default_factory=list)
    total_delay: float = 0.0
    is_dropped: bool = False

    def __post_init__(self):
        self.current_node = self.src
        self.path_taken.append(self.src)


